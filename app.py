def get_row(name):
    key = normalize(name)
    r = df[df["key"]==key]
    return r.iloc[0] if len(r) else None

def style(feature):
    name = feature["properties"]["NAME_3"]
    row = get_row(name)
    if row is None:
        return {"fillColor":"gray", "fillOpacity": 0.5}
    if st.session_state.selected_municipio == name:
        return {"fillColor": "#4fc3f7", "fillOpacity": 1.0, "color": "#00d4ff", "weight": 6, "dashArray": "0"}
    if show_hotspot and row.get("is_hotspot", False):
        return {"fillColor": "#8e7cc3", "fillOpacity": 0.85, "weight": 3}
    val = safe_float(row.get("value"))
    color = "#1a9850" if val < 5 else "#fee08b" if val < 15 else "#b30000"
    return {"fillColor": color, "fillOpacity": 0.75}

