# =========================================
# 3. 데이터 로드
# =========================================
@st.cache_data
def load_data():

    df = pd.read_excel(
        "Rociado Chuquisaca 2025.xlsx",
        sheet_name="RESUMEN_2",
        skiprows=8,
        nrows=31
    )

    df.columns = df.columns.str.replace("\n", " ")
    df.columns = df.columns.str.strip()

    df = df.rename(columns={
        "MUNICIPIO": "municipio",
        "EXIST": "viv_exist",
        "ROC": "viv_roc"
    })

    df = df[df["municipio"].notna()]
    df = df[~df["municipio"].str.contains("TOTAL", na=False)]

    df["viv_roc"] = df["viv_roc"].fillna(0)
    df["viv_exist"] = df["viv_exist"].fillna(0)

    df["coverage"] = df["viv_roc"] / df["viv_exist"].replace(0, 1)

    df["chagas"] = df["coverage"]
    df["dengue"] = df["coverage"] * 0.8
    df["malaria"] = df["coverage"] * 0.5

    # GeoJSON 로드
    with open("gadm41_BOL_3.json") as f:
        geojson_data = json.load(f)

    geojson_data["features"] = [
        f for f in geojson_data["features"]
        if f["properties"].get("NAME_1") == "Chuquisaca"
    ]

    return df, geojson_data

df, geojson_data = load_data()

# =========================================
# 4. 사이드바
# =========================================
st.sidebar.header("⚙️ Configuración")

disease = st.sidebar.radio(
    "Seleccionar enfermedad",
    ["chagas", "dengue", "malaria"]
)

threshold = st.sidebar.slider(
    "Criterios de riesgo",
    0.0, 1.0, 0.2
)

# =========================================
# 5. KPI
# =========================================
col1, col2, col3 = st.columns(3)

col1.metric("📊 Cobertura promedio", f"{df[disease].mean():.2%}")
col2.metric("🏠 Desinfección total", f"{int(df['viv_roc'].sum()):,}")
col3.metric("⚠️ Zona de riesgo", int((df[disease] < threshold).sum()))

# =========================================
# 🔥 6. 매핑 기반 데이터 연결
# =========================================
def get_row(name):

    mapped_name = mapping.get(name, None)

    if mapped_name is None:
        return None

    row = df[df["municipio"].str.upper() == mapped_name]

    if len(row) > 0:
        return row.iloc[0]

    return None

# =========================================
# 7. 스타일
# =========================================
top5 = df.nlargest(5, disease)["municipio"].str.upper().tolist()

def style_function(feature):

    name = feature["properties"]["NAME_3"]
    row = get_row(name)

    if row is None:
        return {"fillColor": "gray", "color": "black", "weight": 1}

    value = row[disease]

    if value < threshold:
        color = "#d73027"
    elif row["municipio"].upper() in top5:
        color = "#4575b4"
    elif value < 0.3:
        color = "#fee08b"
    else:
        color = "#1a9850"

    return {
        "fillColor": color,
        "color": "black",
        "weight": 1,
        "fillOpacity": 0.7
    }

# =========================================
# 8. 지도
# =========================================
st.subheader("🗺️ Chuquisaca Mapa")

m = folium.Map(location=[-19, -65], zoom_start=8)

for feature in geojson_data["features"]:

    name = feature["properties"]["NAME_3"]
    row = get_row(name)

    if row is not None:
        popup_html = f"""
        <b>{name}</b><br>
        Cobertura: {row[disease]:.2%}<br>
        Casas rociadas: {int(row['viv_roc'])}<br>
        Total viviendas: {int(row['viv_exist'])}
        """
    else:
        popup_html = f"<b>{name}</b><br>Sin datos"

    folium.GeoJson(
        feature,
        style_function=style_function,
        popup=folium.Popup(popup_html, max_width=250),
        tooltip=name
    ).add_to(m)

map_data = st_folium(m, width=900, height=500)

